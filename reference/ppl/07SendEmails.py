# MAGIC #### Cell 2 — Helpers

# COMMAND ----------

def sanitize_text(s):

    if s is None:
        return ""

    s = str(s)
    s = s.replace("\x00","")
    s = s.replace("\xa0"," ")

    return s.strip()


def clean_email(addr):

    if addr is None:
        return ""

    addr = sanitize_text(addr)
    addr = addr.replace(" ","")

    if "@" not in addr:
        return ""

    return addr


def esc_sql(val):

    if val is None:
        return ""

    return sanitize_text(val).replace("'","''")


def split_emails(raw):

    if raw is None:
        return []

    parts = re.split(r"[;,]+", raw)

    return [p.strip() for p in parts if p.strip()]

