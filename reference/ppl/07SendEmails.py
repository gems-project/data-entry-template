# MAGIC emails that already have sendStatus = SENT are skipped  
# MAGIC only unsent or previously failed messages are processed  
# MAGIC
# MAGIC This prevents duplicate notifications from being sent.
# MAGIC
# MAGIC Construct Email Messages
