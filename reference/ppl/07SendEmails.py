# MAGIC Record Email Delivery Results
# MAGIC After each sending attempt, the notebook updates the email queue table with the result of the operation.
# MAGIC
# MAGIC The following fields are written to opsEmailQueueQc:
# MAGIC
# MAGIC sendStatus (SENT or FAILED)  
# MAGIC sentTsUtc (timestamp of the sending attempt)  
