# MAGIC %md
# MAGIC #### Cell 2B — Build email message (plain text only)

# COMMAND ----------
# Plain text preserves line breaks reliably across Outlook and other clients.
# HTML was removed because Outlook often collapses or mangles HTML formatting.

def build_email_message(from_email, reply_to, to_email, cc_list, subject, body):

    msg = EmailMessage()

    msg["From"] = from_email
    msg["To"] = to_email

    if cc_list:
        msg["Cc"] = ", ".join(cc_list)

    if reply_to:
        msg["Reply-To"] = reply_to

    msg["Subject"] = sanitize_text(subject)

    plain = sanitize_text(body)
    msg.set_content(plain)

    recipients = [to_email] + (cc_list or [])

    return msg, recipients

