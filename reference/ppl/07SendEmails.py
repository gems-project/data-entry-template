# MAGIC #### Cell 3 — Find the latest emailRunId (the run we will send)

# COMMAND ----------

q = spark.table(emailQueueQcTable)

latestEmailRunId = q.select(
    F.max("emailRunId").alias("emailRunId")
).collect()[0]["emailRunId"]

print("latestEmailRunId:", latestEmailRunId)

# COMMAND ----------

# MAGIC %md
# MAGIC #### Cell 4 — Select “latest run per study” rows to send (no duplicates)
# MAGIC
# MAGIC Rule:
# MAGIC
# MAGIC only rows from latestEmailRunId
# MAGIC
# MAGIC only one row per (studyId, sequence) (if duplicates exist, keep newest createdTsUtc)
# MAGIC
# MAGIC only unsent rows: sentTsUtc IS NULL and (sendStatus is null or not SENT)

# COMMAND ----------

from pyspark.sql.window import Window

q_latest = q.filter(
    F.col("emailRunId") == latestEmailRunId
)

w = Window.partitionBy("studyId","sequence")\
          .orderBy(F.col("createdTsUtc").desc())

to_send = (
    q_latest
    .withColumn("rn", F.row_number().over(w))
    .filter(F.col("rn")==1)
    .drop("rn")
    .filter(
        (F.col("sentTsUtc").isNull()) &
        (F.col("sendStatus").isNull() |
         (F.col("sendStatus")!="SENT"))
    )
)

n = to_send.count()

print(f"Emails to send: {n}")

display(
    to_send.select(
        "studyId",
        "sequence",
        "toEmail",
        "emailSubject",
        "createdTsUtc"
    )
)

# COMMAND ----------

# MAGIC %md
# MAGIC #### Cell 5 — Send emails + write back status to opsEmailQueueQc

# COMMAND ----------

rows = to_send.collect()

print(f"Processing {len(rows)} emails")

for r in rows:

    emailRunId = r["emailRunId"]
    studyId    = r["studyId"]
    sequence   = r["sequence"]

    toEmail = clean_email(r["toEmail"])
    cc_list = split_emails(r["ccEmails"])

    subject = sanitize_text(r["emailSubject"])
    body    = sanitize_text(r["emailBody"])


    if not toEmail:

        err_msg = "Missing toEmail"

        spark.sql(f"""
        UPDATE {emailQueueQcTable}
        SET sendStatus='FAILED',
            sentTsUtc=current_timestamp(),
            sendError='{esc_sql(err_msg)}'
        WHERE emailRunId='{emailRunId}'
        AND studyId='{studyId}'
        AND sequence='{sequence}'
        """)

        continue


    try:

        if not DRY_RUN:

            send_email(
                DEFAULT_FROM_EMAIL,
                DEFAULT_FROM_EMAIL,
                SMTP_PASSWORD,
                toEmail,
                cc_list,
                subject,
                body,
                DEFAULT_REPLY_TO
            )


        spark.sql(f"""
        UPDATE {emailQueueQcTable}
        SET sendStatus='SENT',
            sentTsUtc=current_timestamp(),
            sendError=NULL
        WHERE emailRunId='{emailRunId}'
        AND studyId='{studyId}'
        AND sequence='{sequence}'
        """)

        print(f"[OK] SENT {studyId}-{sequence}")


    except Exception as e:

        err = esc_sql(str(e))

        spark.sql(f"""
        UPDATE {emailQueueQcTable}
        SET sendStatus='FAILED',
            sentTsUtc=current_timestamp(),
            sendError='{err}'
        WHERE emailRunId='{emailRunId}'
        AND studyId='{studyId}'
        AND sequence='{sequence}'
        """)

        print(f"[FAIL] {studyId}-{sequence} -> {err}")