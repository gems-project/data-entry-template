# MAGIC %md
# MAGIC #### Cell 2D — Send email function

# COMMAND ----------

def send_email(
    from_email,
    smtp_user,
    smtp_password,
    to_email,
    cc_list,
    subject,
    body,
    reply_to=None
):

    to_email = clean_email(to_email)

    cc_list = [
        clean_email(x)
        for x in (cc_list or [])
        if clean_email(x)
    ]

    msg, recipients = build_email_message(
        from_email,
        reply_to,
        to_email,
        cc_list,
        subject,
        body
    )

    with smtplib.SMTP(SMTP_SERVER, SMTP_PORT, timeout=30) as server:

        server.ehlo()
        server.starttls()
        server.ehlo()

        server.login(smtp_user, smtp_password)

        server.send_message(
            msg,
            from_addr=from_email,
            to_addrs=recipients
        )

